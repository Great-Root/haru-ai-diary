export type Lang = keyof typeof translations;
export type TransKey = keyof (typeof translations)["en"];

function detectLang(): Lang {
  const saved = localStorage.getItem("haru-lang") as Lang | null;
  if (saved && translations[saved]) return saved;

  // Try browser language first
  const nav = navigator.language.slice(0, 2);
  if (nav === "ko") return "ko";
  if (nav === "ja") return "ja";

  // Fallback: infer from timezone
  const tz = Intl.DateTimeFormat().resolvedOptions().timeZone || "";
  if (tz.startsWith("Asia/Seoul") || tz === "ROK") return "ko";
  if (tz.startsWith("Asia/Tokyo") || tz === "Japan") return "ja";

  return "en";
}

let currentLang: Lang = detectLang();
const listeners = new Set<() => void>();

export function getLang(): Lang {
  return currentLang;
}

export function setLang(lang: Lang) {
  currentLang = lang;
  localStorage.setItem("haru-lang", lang);
  document.documentElement.lang = lang;
  listeners.forEach((fn) => fn());
}

export function t(key: TransKey): string {
  return translations[currentLang][key] || translations.en[key] || key;
}

export function onLangChange(fn: () => void) {
  listeners.add(fn);
  return () => listeners.delete(fn);
}
